package io.ckl.firebaseintegration;

public class FirebaseData {

    private String data;
    private String name;
    private int thisIsABigName;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getThisIsABigName() {
        return thisIsABigName;
    }

    public void setThisIsABigName(int thisIsABigName) {
        this.thisIsABigName = thisIsABigName;
    }
}
